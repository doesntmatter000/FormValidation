import { ToastContainer, toast } from 'react-toastify';

export function sucessOrError(bool:boolean):Function {
    const success = () => toast.success("Success!",{
        position: "top-right",
        autoClose: 5000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
        theme: "light",
    });

    const error = () => toast.error('Something went wrong try again!', {
        position: "top-right",
        autoClose: 5000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
        theme: "light",
        });

    return bool ? success : error
}

